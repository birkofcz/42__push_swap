#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*p;
	int		i;

	i = 0;
	p = (char *)str;
	while (n > 0)
	{
		p[i] = c;
		i++;
		n--;
	}
	return (str);
}
